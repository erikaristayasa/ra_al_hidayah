import '../../../statics/statics.dart';
import '../../domain/entities/cost_breakdown_enity.dart';

class CostBreakdownModel extends CostBreakdown {
  const CostBreakdownModel({
    required int id,
    required int reRegistrationId,
    required GradeType type,
    required int maleActivity,
    required int maleStationary,
    required int maleSpp,
    required int maleUniform,
    required int maleHood,
    required int maleBuildingMoney,
    required int maleTotalCost,
    required int femaleActivity,
    required int femaleStationary,
    required int femaleSpp,
    required int femaleUniform,
    required int femaleHood,
    required int femaleBuildingMoney,
    required int femaleTotalCost,
  }) : super(
          id: id,
          reRegistrationId: reRegistrationId,
          type: type,
          maleActivity: maleActivity,
          maleStationary: maleStationary,
          maleSpp: maleSpp,
          maleUniform: maleUniform,
          maleHood: maleHood,
          maleBuildingMoney: maleBuildingMoney,
          maleTotalCost: maleTotalCost,
          femaleActivity: femaleActivity,
          femaleStationary: femaleStationary,
          femaleSpp: femaleSpp,
          femaleUniform: femaleUniform,
          femaleHood: femaleHood,
          femaleBuildingMoney: femaleBuildingMoney,
          femaleTotalCost: femaleTotalCost,
        );

  factory CostBreakdownModel.fromJson(Map<String, dynamic> json) => CostBreakdownModel(
        id: json['id'],
        reRegistrationId: json['jpu_id'],
        type: (json['tipe'] as String).gradeType,
        maleActivity: json['laki_laki_kegiatan'],
        maleStationary: json['laki_laki_alat_tulis_dan_buku_paket'],
        maleSpp: json['laki_laki_spp'],
        maleUniform: json['laki_laki_seragam_5_stel'],
        maleHood: json['laki_laki_kerudung'],
        maleBuildingMoney: json['laki_laki_uang_bangunan'],
        maleTotalCost: json['laki_laki_total_biaya'],
        femaleActivity: json['perempuan_kegiatan'],
        femaleStationary: json['perempuan_alat_tulis_dan_buku_paket'],
        femaleSpp: json['perempuan_spp'],
        femaleUniform: json['perempuan_seragam_5_stel'],
        femaleHood: json['perempuan_kerudung'],
        femaleBuildingMoney: json['perempuan_uang_bangunan'],
        femaleTotalCost: json['perempuan_total_biaya'],
      );
}
